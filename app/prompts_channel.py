def build_channel_rewrite_user_message(source_text: str) -> str:
    return f"Входной текст новости:\n\n{source_text}"


def _truncate(text: str, limit: int) -> str:
    text = (text or "").strip()
    return text if len(text) <= limit else text[:limit].rstrip() + "…"


def build_exemplar_block(
    best_examples: list[dict],
    worst_examples: list[dict],
    *,
    body_char_limit: int = 600,
) -> str:
    """Формирует блок «эталонов» для подачи LLM как ориентира по стилю.

    best_examples: посты с rating >= 4 — образец для подражания.
    worst_examples: посты с rating <= 2 — пример как НЕ надо.
    Возвращает пустую строку если оба списка пусты.
    """
    if not best_examples and not worst_examples:
        return ""

    parts: list[str] = ["\n\n=== Эталоны (учись на оценках админа) ==="]

    if best_examples:
        parts.append("\nХОРОШИЕ ПОСТЫ (так пиши):")
        for i, ex in enumerate(best_examples, 1):
            title = _truncate(str(ex.get("title") or ""), 200)
            body = _truncate(str(ex.get("post_text") or ""), body_char_limit)
            rating = int(ex.get("rating") or 0)
            comment = str(ex.get("comment") or "").strip()
            block = f"\n[{i}] ⭐{rating}/5  «{title}»\n{body}"
            if comment:
                block += f"\nКомментарий админа: {comment}"
            parts.append(block)

    if worst_examples:
        parts.append("\nПЛОХИЕ ПОСТЫ (так НЕ пиши):")
        for i, ex in enumerate(worst_examples, 1):
            title = _truncate(str(ex.get("title") or ""), 200)
            body = _truncate(str(ex.get("post_text") or ""), 350)
            rating = int(ex.get("rating") or 0)
            comment = str(ex.get("comment") or "").strip()
            block = f"\n[{i}] ⭐{rating}/5  «{title}»\n{body}"
            if comment:
                block += f"\nЧто не так: {comment}"
            parts.append(block)

    parts.append("\n=== /Эталоны ===\n")
    return "\n".join(parts)
